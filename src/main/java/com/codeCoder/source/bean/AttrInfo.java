package com.codeCoder.source.bean;

public class AttrInfo {

	private String attrTitle;
	private String attrName;
	private String attrType;
	private String columnType;
	private String columnChecks;

	public String getAttrTitle() {
		return attrTitle;
	}

	public void setAttrTitle(String attrTitle) {
		this.attrTitle = attrTitle;
	}

	public String getAttrName() {
		return attrName;
	}

	public void setAttrName(String attrName) {
		this.attrName = attrName;
	}

	public String getAttrType() {
		return attrType;
	}

	public void setAttrType(String attrType) {
		this.attrType = attrType;
	}

	public String getColumnType() {
		return columnType;
	}

	public void setColumnType(String columnType) {
		this.columnType = columnType;
	}

	public String getColumnChecks() {
		return columnChecks;
	}

	public void setColumnChecks(String columnChecks) {
		this.columnChecks = columnChecks;
	}

}
